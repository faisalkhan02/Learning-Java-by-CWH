package Chapter5;// package learning java.Chapter 5;

public class do_while_loop {
    public static void main(String[] args) {
        int a= 0;
        while(a<5){
            System.out.println(a);
            a++;
        }
        System.out.println("=======Do while loop======");
        int  b = 10;
        do{
            System.out.println(b);
            b++;
        }while (b<5);
    }
}
